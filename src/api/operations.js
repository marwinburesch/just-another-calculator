export function add(a, b) {
  const result = parseInt(a) + parseInt(b);
  return result;
}
export function substract(a, b) {
  const result = parseInt(a) - parseInt(b);
  return result;
}
export function multiply(a, b) {
  const result = parseInt(a) * parseInt(b);
  return result;
}
export function divide(a, b) {
  const result = parseInt(a) / parseInt(b);
  return result;
}
